// Export function to get mega questions
export const getMegaQuestions = (count, difficulty = 'easy') => {
  const allQuestions = [];
  
  Object.keys(megaQuestions).forEach(category => {
    if (megaQuestions[category][difficulty]) {
      allQuestions.push(...megaQuestions[category][difficulty]);
    }
  });
  
  return allQuestions
    .sort(() => Math.random() - 0.5)
    .slice(0, count)
    .map(q => ({
      question: q.q,
      options: q.o,
      correct: q.c
    }));
};

export default megaQuestions;
